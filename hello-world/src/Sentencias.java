
public class Sentencias {

	public static void main(String[] args) {
		// Se puede asumir una instruccion en cuanto se encuentre con un punto y comaa
		
		// Instrucciones de expresion
		// Mientras la sintaxis sea correcta, puede estar la sentencia en más de una linea
		int valueA = 8933; // Declaración y Asignacion (Ya al momento de darle un valor)
		valueA++;		   // Incremento

		// Instrucciones de declaración de variable
		int value;						// Declaracion de un int
		double doubleValue = 8933.234;	// Declaracion y asignacion de una variable double
		String valueString;				// Declaración de una variable String y todo esa es una instruccion
		
		// Bloque de instrucciones
		/* Todo lo que este dentro de un par de llaves
		 * {
		 *    // Todo lo que este dentro de un par de llaves sería un bloque de instrucciones
		 *    // Y siempre van por pares, la de apertura tiene que ir acompañada con la de cierre
		 * }
		 */
	}

}